import express from "express";

const app = express();

app.get("/", (req, res) => {
  // lets do some work
  let sum = 0;
  for (let i = 0; i < 10000; i++) {
    sum = sum + i;
  }

  console.log(`Number :: ${sum}`);

  res.status(200).send({
    message: "HIII",
  });
});

app.listen(3000, () => {
  console.log("Server is running on port 3000");
});

